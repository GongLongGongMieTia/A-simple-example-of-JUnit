package com;

public class Calculate {

	public double add(double n1, double n2) { 
        return n1 + n2; 
    }
	
	public double min(double n1, double n2) { 
        return n1 - n2; 
    }
	
	public double mul(double n1, double n2) { 
        return n1 * n2; 
    }
	
	public double dev(double n1, double n2) { 
        return n1 / n2; 
    }
}
